const STORAGE_KEY = "rajo-ai-language";

type LanguageContextValue = {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => void;
  t: (key: string, values?: Record<string, string | number>) => string;
};

const LanguageContext = createContext<LanguageContextValue | undefined>(undefined);

function readStoredLanguage(): AppLanguage {
  if (typeof window === "undefined") return "en";
  const stored = window.localStorage.getItem(STORAGE_KEY);
  return stored === "so" || stored === "en" ? stored : "en";
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>(() => readStoredLanguage());

  useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language === "so" ? "so" : "en";
  }, [language]);

  useEffect(() => {
    if (language !== "so") return;

    let frame = window.requestAnimationFrame(() => translateVisibleDocument(language));
    const translateSoon = () => {
      window.cancelAnimationFrame(frame);
      frame = window.requestAnimationFrame(() => translateVisibleDocument(language));
    };
    const observer = new MutationObserver(translateSoon);
    observer.observe(document.body, {
      childList: true,
      subtree: true,
      characterData: true,
      attributes: true,
      attributeFilter: ["placeholder", "aria-label", "title"],
    });

    return () => {
      window.cancelAnimationFrame(frame);
      observer.disconnect();
    };
  }, [language]);

  const value = useMemo<LanguageContextValue>(() => ({
    language,
    setLanguage: (nextLanguage) => setLanguageState(nextLanguage),
    t: (key, values) => {
      const template = translations[language][key] ?? translations.en[key] ?? key;
      if (!values) return template;
      return Object.entries(values).reduce(
        (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
        template,
      );
    },
  }), [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) throw new Error("useLanguage must be used inside LanguageProvider");
  return context;
}

const domEnglishToSomali = new Map<string, string>(
  Object.values(translations.en)
    .map((english) => {
      const key = Object.keys(translations.en).find((item) => translations.en[item] === english);
      return key ? [english, translations.so[key] ?? english] : null;
    })
    .filter((entry): entry is [string, string] => Boolean(entry)),
);

function translateVisibleDocument(language: AppLanguage) {
  if (language === "en") return;

  const skipTags = new Set(["SCRIPT", "STYLE", "NOSCRIPT", "TEXTAREA"]);
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || skipTags.has(parent.tagName)) return NodeFilter.FILTER_REJECT;
      if (!node.textContent?.trim()) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    },
  });

  const textNodes: Text[] = [];
  while (walker.nextNode()) textNodes.push(walker.currentNode as Text);

  for (const node of textNodes) {
    const nextText = translateText(node.textContent ?? "");
    if (node.textContent !== nextText) node.textContent = nextText;
  }

  for (const element of Array.from(document.querySelectorAll<HTMLElement>("[placeholder], [aria-label], [title]"))) {
    for (const attr of ["placeholder", "aria-label", "title"] as const) {
      const value = element.getAttribute(attr);
      if (!value) continue;
      const nextValue = translateText(value);
      if (value !== nextValue) element.setAttribute(attr, nextValue);
    }
  }
}

function translateText(value: string): string {
  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  const text = value.trim().replace(/\s+/g, " ");

  const exact = domEnglishToSomali.get(text) ?? extraDomTranslations[text];
  if (exact) return `${leading}${exact}${trailing}`;

  for (const [pattern, replacer] of dynamicDomTranslations) {
    const match = text.match(pattern);
    if (match) return `${leading}${replacer(match)}${trailing}`;
  }

  return value;
}

